// Flatten all skills for quick lookup
const SKILL_TO_CATEGORY: Record<string, string> = {};
Object.entries(SKILLS_TAXONOMY).forEach(([category, skills]) => {
  skills.forEach(skill => {
    SKILL_TO_CATEGORY[skill.toLowerCase()] = category;
  });
});

export function categorizeSkill(skillName: string): string {
  const normalized = skillName.toLowerCase().trim();
  
  // Direct match
  if (SKILL_TO_CATEGORY[normalized]) {
    return SKILL_TO_CATEGORY[normalized];
  }
  
  // Partial match (e.g., "React.js" matches "React")
  for (const [skill, category] of Object.entries(SKILL_TO_CATEGORY)) {
    if (normalized.includes(skill) || skill.includes(normalized)) {
      return category;
    }
  }
  
  // Fallback
  return 'Other';
}

// Get all skills for a category
export function getSkillsByCategory(category: string): string[] {
  return SKILLS_TAXONOMY[category as keyof typeof SKILLS_TAXONOMY] || [];
}

// Get all categories
export function getAllCategories(): string[] {
  return Object.keys(SKILLS_TAXONOMY);
}

// Search skills across all categories
export function searchSkills(query: string): Array<{ skill: string; category: string }> {
  const normalized = query.toLowerCase();
  const results: Array<{ skill: string; category: string }> = [];
  
  Object.entries(SKILLS_TAXONOMY).forEach(([category, skills]) => {
    skills.forEach(skill => {
      if (skill.toLowerCase().includes(normalized)) {
        results.push({ skill, category });
      }
    });
  });
  
  return results;
}
